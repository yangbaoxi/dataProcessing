// 语法

// string.charCodeAt(index)

// 参数

// index 必需 => 表示字符串中某个位置的数字，即字符在字符串中的下标

// 说明

// charCodeAt() 方法可返回指定位置的字符的 Unicode 编码。

// 示例

let str1 = 'a';
let str2 = 'A';

let n1 = str1.charCodeAt();
let n2 = str2.charCodeAt();


console.log(n1);// 97

console.log(n2);// 65
