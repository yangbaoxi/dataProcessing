// 语法

// string.concat(string1, string2, ..., stringX)

// 参数

// string1, string2, ..., stringX 必须 => 将被连接为一个字符串的一个或多个字符串对象。

// 说明

// concat() 方法用于连接两个或多个字符串。
// 方法没有改变原有字符串，但是会返回连接两个或多个字符串新字符串

// 示例

let str1 = "Hello";
let str2 = "World";

let a = str1.concat(str2);

console.log(a);// HelloWorld

